Add-Type -AssemblyName System.IO.Compression.FileSystem

function Load-DocXml($path) {
    $zip = [System.IO.Compression.ZipFile]::OpenRead($path)
    $entry = $zip.GetEntry('word/document.xml')
    $reader = New-Object System.IO.StreamReader($entry.Open())
    $xml = $reader.ReadToEnd()
    $reader.Close(); $zip.Dispose()
    [xml]$doc = $xml
    $ns = New-Object System.Xml.XmlNamespaceManager($doc.NameTable)
    $ns.AddNamespace('w','http://schemas.openxmlformats.org/wordprocessingml/2006/main')
    return @($doc, $ns)
}

function Save-DocXml($path, $doc) {
    $zip = [System.IO.Compression.ZipFile]::Open($path, 'Update')
    $entry = $zip.GetEntry('word/document.xml')
    if ($entry) { $entry.Delete() }
    $newEntry = $zip.CreateEntry('word/document.xml')
    $writer = New-Object System.IO.StreamWriter($newEntry.Open(), [System.Text.UTF8Encoding]::new($false))
    $writer.Write($doc.OuterXml)
    $writer.Close(); $zip.Dispose()
}

function Set-RunText($doc, $ns, $run, $text) {
    $wNs = $ns.LookupNamespace('w')
    $rPr = $run.SelectSingleNode('w:rPr', $ns)
    if (-not $rPr) {
        $rPr = $doc.CreateElement('w','rPr',$wNs)
        $run.PrependChild($rPr) | Out-Null
    }
    $hl = $rPr.SelectSingleNode('w:highlight', $ns)
    if ($hl) { $rPr.RemoveChild($hl) | Out-Null }
    $color = $rPr.SelectSingleNode('w:color', $ns)
    if (-not $color) {
        $color = $doc.CreateElement('w','color',$wNs)
        $rPr.AppendChild($color) | Out-Null
    }
    $null = $color.SetAttribute('val', $wNs, 'FF0000')

    $ts = $run.SelectNodes('.//w:t', $ns)
    if ($ts.Count -eq 0) {
        $tNode = $doc.CreateElement('w','t',$wNs)
        $tNode.InnerText = $text
        $run.AppendChild($tNode) | Out-Null
    } else {
        $ts[0].InnerText = $text
        for ($i = $ts.Count - 1; $i -ge 1; $i--) {
            $ts[$i].ParentNode.RemoveChild($ts[$i]) | Out-Null
        }
    }
}

function Apply-Map($doc, $ns, $map, $onlyHighlighted = $true) {
    $query = if ($onlyHighlighted) { "//w:r[w:rPr/w:highlight[@w:val='yellow']]" } else { "//w:r" }
    $runs = $doc.SelectNodes($query, $ns)
    foreach ($run in $runs) {
        $text = ($run.SelectNodes('.//w:t', $ns) | ForEach-Object { $_.InnerText }) -join ''
        if ($map.ContainsKey($text)) {
            Set-RunText $doc $ns $run $map[$text]
        }
    }
}

function Redden-Highlights($doc, $ns) {
    $runs = $doc.SelectNodes("//w:r[w:rPr/w:highlight[@w:val='yellow']]", $ns)
    foreach ($run in $runs) {
        $text = ($run.SelectNodes('.//w:t', $ns) | ForEach-Object { $_.InnerText }) -join ''
        Set-RunText $doc $ns $run $text
    }
}

function Remove-AllHighlights($doc, $ns) {
    $nodes = $doc.SelectNodes('//w:highlight', $ns)
    foreach ($node in $nodes) {
        $parent = $node.ParentNode
        if ($parent -ne $null) { $parent.RemoveChild($node) | Out-Null }
    }
}

function Get-NodeText($node, $ns) {
    ($node.SelectNodes('.//w:t', $ns) | ForEach-Object { $_.InnerText }) -join ''
}

function Set-CellText($doc, $ns, $cell, $text) {
    $wNs = $ns.LookupNamespace('w')
    $cell.SelectNodes('w:p', $ns) | ForEach-Object { $cell.RemoveChild($_) | Out-Null }
    $p = $doc.CreateElement('w','p',$wNs)
    $r = $doc.CreateElement('w','r',$wNs)
    $rPr = $doc.CreateElement('w','rPr',$wNs)
    $color = $doc.CreateElement('w','color',$wNs)
    $null = $color.SetAttribute('val', $wNs, 'FF0000')
    $rPr.AppendChild($color) | Out-Null
    $r.AppendChild($rPr) | Out-Null
    $t = $doc.CreateElement('w','t',$wNs)
    $t.InnerText = $text
    $r.AppendChild($t) | Out-Null
    $p.AppendChild($r) | Out-Null
    $cell.AppendChild($p) | Out-Null
}

function Set-ParagraphText($doc, $ns, $p, $text) {
    $wNs = $ns.LookupNamespace('w')
    $runs = $p.SelectNodes('./w:r', $ns)
    if ($runs.Count -eq 0) {
        $r = $doc.CreateElement('w','r',$wNs)
        $p.AppendChild($r) | Out-Null
        Set-RunText $doc $ns $r $text
        return
    }
    Set-RunText $doc $ns $runs[0] $text
    for ($i = $runs.Count - 1; $i -ge 1; $i--) {
        $runs[$i].ParentNode.RemoveChild($runs[$i]) | Out-Null
    }
}

